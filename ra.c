#include "push_swap.h"

void	ra(t_stack *stack)
{
	int	temp;
	int	i;

	i = 0;
	temp = stack->stack_a[0];
	while (i < stack->size_a - 1)
	{
		stack->stack_a[i] = stack->stack_a[i + 1];
		i++;
	}
	stack->stack_a[i] = temp;
	write(1, "ra\n", 3);
	stack->moves++;
}
